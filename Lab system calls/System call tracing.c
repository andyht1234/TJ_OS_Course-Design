uint64
sys_trace(void)
{
    int sys_trace_mask;
    if(argint(0,&sys_trace_mask)<0) //从寄存器拿参数
    {
        return -1;
    }
    myproc()->trace_mask=sys_trace_mask;    //保存参数
    return 0;
}

